/**
 *
 *	\fn BOOL TestSectorBufEncryption(PCRYPTO_INFO ci)
 *	\brief Encrypt same value of data to test if the encryption algorithm woks well
 *	\param[in] PCRYPTO_INFO ci
 *	\return BOOL FALSE 	if (!TestLegacySectorBufEncryption(ci))
 *			FALSE if (EAInit(ci->ea, key1, ci->ks) != ERR_SUCCESS)
 *			FALSE if (!EAInitMode(ci))
 *			FALSE if (crc != 0x05eacf7d)
 *			FALSE if (crc != 0x5b5926d9)
 *			FALSE if (crc != 0xcf0cfdd1)
 *			FALSE if (crc != 0xe82865a8)
 *			FALSE if (crc == 0x9f5edd58)
 *			FALSE if (GetCrc32(buf, sizeof (buf)) != 0x9f5edd58)
 *			FALSE if (EAInit(ci->ea, key1, ci->ks) != ERR_SUCCESS)
 *			FALSE if (!EAInitMode(ci))
 *			FALSE if (crc != 0x5d31eec2)
 *			FALSE if (crc == 0x9f5edd58)
 *			1 otherwise
 *
 */
BOOL TestSectorBufEncryption(PCRYPTO_INFO ci)
{
	unsigned char buf[ENCRYPTION_DATA_UNIT_SIZE * 4];
	unsigned int i;
	char name[64];
	unsigned __int32 crc;
	UINT64_STRUCT unitNo;
	uint32 nbrUnits;
	unsigned __int64 writeOffset;
	int testCase = 0;
	int nTestsPerformed = 0;

	static unsigned char key1[] =
	{
		0x27, 0x18, 0x28, 0x18, 0x28, 0x45, 0x90, 0x45, 0x23, 0x53, 0x60, 0x28, 0x74, 0x71, 0x35, 0x26, 0x62, 0x49, 0x77, 0x57, 0x24, 0x70, 0x93, 0x69, 0x99, 0x59, 0x57, 0x49, 0x66, 0x96, 0x76, 0x27,
		0x31, 0x41, 0x59, 0x26, 0x53, 0x58, 0x97, 0x93, 0x23, 0x84, 0x62, 0x64, 0x33, 0x83, 0x27, 0x95, 0x02, 0x88, 0x41, 0x97, 0x16, 0x93, 0x99, 0x37, 0x51, 0x05, 0x82, 0x09, 0x74, 0x94, 0x45, 0x92,
		0x00, 0x00, 0x00, 0xff, 0xff, 0xff, 0xff, 0xff, 0x00, 0x00, 0x00, 0x00, 0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0a, 0x0b, 0x0c, 0x0d, 0x0e, 0x0f, 0x10, 0x11, 0x12, 0x13
	};

	if (!TestLegacySectorBufEncryption(ci))
		return FALSE;


	/* Encryption/decryption of data units (typically, volume data sectors) */

	nbrUnits = sizeof (buf) / ENCRYPTION_DATA_UNIT_SIZE;

	/* The buffer can accommodate 4 data units and we'll test 4 cases by "scrolling". The data unit 0xFFFFFFFFFF
	will "move" from the start of the buffer to its end. For a 512-byte data unit, the byte offset 562949953420800
	corresponds to the data unit 0xFFFFFFFFFF. */
	for (writeOffset = 562949953420800ULL;
		writeOffset > 562949953420800ULL - nbrUnits * ENCRYPTION_DATA_UNIT_SIZE;
		writeOffset -= ENCRYPTION_DATA_UNIT_SIZE)
	{
		unitNo.Value = writeOffset / ENCRYPTION_DATA_UNIT_SIZE;

		// Test all EAs that support this mode of operation
		for (ci->ea = EAGetFirst(); ci->ea != 0; ci->ea = EAGetNext(ci->ea))
		{
			for (ci->mode = EAGetFirstMode(ci->ea); ci->mode != 0; ci->mode = EAGetNextMode(ci->ea, ci->mode))
			{
				if (!EAIsModeSupported(ci->ea, ci->mode))
					continue;

				EAGetName(name, ci->ea);

				if (EAInit(ci->ea, key1, ci->ks) != ERR_SUCCESS)
					return FALSE;

				for (i = 0; i < sizeof (ci->k2); i++)
					ci->k2[i] = (unsigned char)i;

				memcpy(&ci->k2, XTS_vectors[XTS_TEST_COUNT - 1].key2, sizeof (XTS_vectors[XTS_TEST_COUNT - 1].key2));

				if (!EAInitMode(ci))
					return FALSE;

				// Each data unit will contain the same plaintext
				for (i = 0; i < nbrUnits; i++)
				{
					memcpy((unsigned char *)buf + i * ENCRYPTION_DATA_UNIT_SIZE,
						XTS_vectors[XTS_TEST_COUNT - 1].plaintext,
						ENCRYPTION_DATA_UNIT_SIZE);
				}

				EncryptDataUnits(buf, &unitNo, nbrUnits, ci);

				crc = GetCrc32(buf, sizeof (buf));

				if (strcmp(name, "GOST 28147-89") == 0)
				{
					switch (testCase)
					{
					case 0:
#ifdef GOST_DYNAMIC_SBOXES
						if (crc != 0x05eacf7d)
							return FALSE;
#endif
						nTestsPerformed++;
						break;
					case 1:
#ifdef GOST_DYNAMIC_SBOXES
						if (crc != 0x5b5926d9)
							return FALSE;
#endif
						nTestsPerformed++;
						break;
					case 2:
#ifdef GOST_DYNAMIC_SBOXES
						if (crc != 0xcf0cfdd1)
							return FALSE;
#endif
						nTestsPerformed++;
						break;
					case 3:
#ifdef GOST_DYNAMIC_SBOXES
						if (crc != 0xe82865a8)
							return FALSE;
#endif
						nTestsPerformed++;
						break;
					}
				}
				else if (strcmp(name, "Gost Grasshopper") == 0)
					nTestsPerformed++;

				if (crc == 0x9f5edd58)
					return FALSE;

				DecryptDataUnits(buf, &unitNo, nbrUnits, ci);

				if (GetCrc32(buf, sizeof (buf)) != 0x9f5edd58)
					return FALSE;

				nTestsPerformed++;
			}
		}
		testCase++;
	}

	/* Encryption/decryption of a buffer (typically, a volume header) */

	nbrUnits = sizeof (buf) / ENCRYPTION_DATA_UNIT_SIZE;

	// Test all EAs that support this mode of operation
	for (ci->ea = EAGetFirst(); ci->ea != 0; ci->ea = EAGetNext(ci->ea))
	{
		for (ci->mode = EAGetFirstMode(ci->ea); ci->mode != 0; ci->mode = EAGetNextMode(ci->ea, ci->mode))
		{
			if (!EAIsModeSupported(ci->ea, ci->mode))
				continue;

			EAGetName(name, ci->ea);

			if (EAInit(ci->ea, key1, ci->ks) != ERR_SUCCESS)
				return FALSE;

			memcpy(&ci->k2, XTS_vectors[XTS_TEST_COUNT - 1].key2, sizeof (XTS_vectors[XTS_TEST_COUNT - 1].key2));

			if (!EAInitMode(ci))
				return FALSE;

			// Each data unit will contain the same plaintext
			for (i = 0; i < nbrUnits; i++)
			{
				memcpy((unsigned char *)buf + i * ENCRYPTION_DATA_UNIT_SIZE,
					XTS_vectors[XTS_TEST_COUNT - 1].plaintext,
					ENCRYPTION_DATA_UNIT_SIZE);
			}

			EncryptBuffer(buf, sizeof (buf), ci);

			crc = GetCrc32(buf, sizeof (buf));

			if (strcmp(name, "GOST 28147-89") == 0)
			{
#ifdef GOST_DYNAMIC_SBOXES
				if (crc != 0x5d31eec2)
					return FALSE;
#endif
				nTestsPerformed++;
			}
			else if (strcmp(name, "Gost Grasshopper") == 0)
				nTestsPerformed++;


			if (crc == 0x9f5edd58)
				return FALSE;

			DecryptBuffer(buf, sizeof (buf), ci);

			if (GetCrc32(buf, sizeof (buf)) != 0x9f5edd58)
				return FALSE;

			nTestsPerformed++;
		}
	}

	return 1;
	//return (nTestsPerformed == 20); // 2 * number of ea
}

/**
 *
 *	\fn BOOL TestLegacySectorBufEncryption (PCRYPTO_INFO ci)
 *	\brief Test the legacy mode for boot encryption
 *	\param[in] PCRYPTO_INFO ci
 *	\return BOOL FALSE if (crc == 0xb70b4c26)
 *			FALSE if (EAInit (ci->ea, (unsigned char *)buf, ci->ks) == ERR_CIPHER_INIT_FAILURE)
 *			FALSE if (GetCrc32 (buf, sizeof (buf)) != 0xb70b4c26)
 *			FALSE if (crc == 0xb70b4c26)
 *			1 otherwise
 *
 */
BOOL TestLegacySectorBufEncryption (PCRYPTO_INFO ci)
{
	unsigned char buf [ENCRYPTION_DATA_UNIT_SIZE * 2];
	unsigned int i;
	char name[64];
	unsigned __int32 crc;
	UINT64_STRUCT unitNo;
	uint32 nbrUnits;
	int blockSize;
	int nTestsPerformed = 0;

	unitNo.Value = 0x0234567890ABCDEFull;
	nbrUnits = sizeof (buf) / ENCRYPTION_DATA_UNIT_SIZE;

	for (i = 0; i < sizeof (buf); i++)
		buf[i] = (unsigned char) i;

	for (i = 0; i < sizeof (ci->k2); i++)
		ci->k2[i] = (unsigned char) i;

	// Test all EAs
	for (ci->ea = EAGetFirst (); ci->ea != 0; ci->ea = EAGetNext (ci->ea))
	{
		EAGetName (name, ci->ea);
		blockSize = CipherGetBlockSize (EAGetFirstCipher (ci->ea));

		if (EAInit (ci->ea, (unsigned char *)buf, ci->ks) == ERR_CIPHER_INIT_FAILURE)
			return FALSE;

		// Test all deprecated modes of operation
		for (ci->mode = EAGetFirstMode (ci->ea);
			ci->mode != 0;
			ci->mode = EAGetNextMode (ci->ea, ci->mode))
		{
			// Skip modes that are not deprecated
			if (ci->mode == XTS || ci->mode == XTS8)
				continue;

			EncryptDataUnits (buf, &unitNo, nbrUnits, ci);
			crc = GetCrc32 (buf, sizeof (buf));

			if (crc == 0xb70b4c26)
				return FALSE;

			DecryptDataUnits (buf, &unitNo, nbrUnits, ci);

			if (GetCrc32 (buf, sizeof (buf)) != 0xb70b4c26)
				return FALSE;

			nTestsPerformed++;

			EncryptBuffer (buf, sizeof (buf), ci);
			crc = GetCrc32 (buf, sizeof (buf));

			if (crc == 0xb70b4c26)
				return FALSE;

			DecryptBuffer (buf, sizeof (buf), ci);

			if (GetCrc32 (buf, sizeof (buf)) != 0xb70b4c26)
				return FALSE;

			nTestsPerformed++;
		}
	}
	return 1;//(nTestsPerformed == 86);
}


/**
 *
 *	\fn static BOOL DoAutoTestAlgorithms (void)
 *	\brief Do all the algorithm tests.
 *	\return static BOOL FALSE if (!ci)
 *			!bFailed otherwise
 *
 */
static BOOL DoAutoTestAlgorithms (void)
{
	PCRYPTO_INFO ci;
	BOOL bFailed = FALSE;

	ci = crypto_open ();
	if (!ci)
		return FALSE;

	memset (ci, 0, sizeof (*ci));

	/* GOST Cipher test here */
	
	/* PKCS #5 and HMACs */
	if (!test_pkcs5 ())
		bFailed = TRUE;

	/* CRC-32 */
	if (!crc32_selftests ())
		bFailed = TRUE;

	/* GF multiplicator */
#if 0
	if (!GfMulSelfTest ())
		bFailed = TRUE;
#endif

	/* Sector and buffer related algorithms */
	if (!TestSectorBufEncryption (ci))
		bFailed = TRUE;
	
	crypto_close (ci);
	return !bFailed;
}


/**
 *
 *	\fn BOOL AutoTestAlgorithms (void)
 *	\brief Launch the auto test algorithm before EnableHwEncryption and after setting it.
 *	\return BOOL result
 *
 */
BOOL AutoTestAlgorithms (void)
{
	BOOL result = TRUE;
	BOOL hwEncryptionEnabled = IsHwEncryptionEnabled();

	EnableHwEncryption (FALSE);

	if (!DoAutoTestAlgorithms())
		result = FALSE;

	EnableHwEncryption (TRUE);

	if (!DoAutoTestAlgorithms())
		result = FALSE;

	EnableHwEncryption (hwEncryptionEnabled);
	return result;
}

